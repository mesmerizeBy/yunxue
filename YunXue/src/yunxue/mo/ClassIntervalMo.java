package yunxue.mo;

//对应开课时段表
public class ClassIntervalMo {
	
	private int intervalNo;
	private String intervalDate;
	private String intervalTime;
	
	public int getIntervalNo() {
		return intervalNo;
	}
	public void setIntervalNo(int intervalNo) {
		this.intervalNo = intervalNo;
	}
	public String getIntervalDate() {
		return intervalDate;
	}
	public void setIntervalDate(String intervalDate) {
		this.intervalDate = intervalDate;
	}
	public String getIntervalTime() {
		return intervalTime;
	}
	public void setIntervalTime(String intervalTime) {
		this.intervalTime = intervalTime;
	}
	

}
