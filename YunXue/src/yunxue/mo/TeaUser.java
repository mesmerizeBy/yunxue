package yunxue.mo;

public class TeaUser extends UserMo {
	
    private String teaSat;
    private String teaStyle;
    private String teaMoney;
    private String teaClasses;
    
	public String getTeaSat() {
		return teaSat;
	}
	public void setTeaSat(String teaSat) {
		this.teaSat = teaSat;
	}
	public String getTeaStyle() {
		return teaStyle;
	}
	public void setTeaStyle(String teaStyle) {
		this.teaStyle = teaStyle;
	}
	public String getTeaMoney() {
		return teaMoney;
	}
	public void setTeaMoney(String teaMoney) {
		this.teaMoney = teaMoney;
	}
	public String getTeaClasses() {
		return teaClasses;
	}
	public void setTeaClasses(String teaClasses) {
		this.teaClasses = teaClasses;
	}
}
